package basicofjava.primitivetype;

public class PrimitiveDataTypeExample {

    public static void main(String[] args) {

        //byte data type
        byte b = -128;       //1 byte

        //short data type
        short s = -32768;      //2 byte

        //int data type
        int i = 2147483647;    //4 byte

        //long data type
        long lg = 10000009873456l;      // 8 byte

        //float data type
        float point = 10.5f;        //4 byte

        //double data type
        double dub = 45678.456783456789098; // 8 byte

        //boolean data type
        boolean boo = true;    //1 byte

        //char data type
        char c = 't';       //2 byte

        System.out.println(c);
    }
}

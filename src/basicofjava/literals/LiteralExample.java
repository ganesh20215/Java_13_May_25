package basicofjava.literals;

public class LiteralExample {

    public static void main(String[] args) {

        //Decimal literals (Base 10) [0 to 9]
        int x = 9102;

        //Octal literals (Base 8) [0 to 7]
        int c = 0756;

        //Hexa decimal liter (Base 16) [0 to 15]
        int y = 0X56af;

        //Binary literal (Base 2) [0 & 1]
        int z = 0B110;

        //floating point literal
        float d = 10.5F;

        //char literal [0 to 127]
        char m = 91;

        //String literal
        String company = "Edso Services";

        System.out.println(company);
    }
}
